# ----- quick spin: —Ä–µ–∑—É–ª—å—Ç–∞—Ç = —Å–µ—Ä–µ–¥–∏–Ω–∞, –±–µ–∑ –∫–æ–ª–µ—Å–∞/–∞–Ω–∏–º–∞—Ü–∏–∏ -----
def spin_handler(call):
    chat_id = call.message.chat.id
    user_id = call.from_user.id

    if chat_id in spin_locks:
        bot.answer_callback_query(call.id, "–°–ø–∏–Ω —É–∂–µ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è. –ü–æ–¥–æ–∂–¥–∏—Ç–µ...", show_alert=False)
        return

    bal = get_balance(user_id)
    bet = 1
    if bal < bet:
        bot.answer_callback_query(call.id, "–ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ —Å—Ä–µ–¥—Å—Ç–≤ –¥–ª—è —Å—Ç–∞–≤–∫–∏.", show_alert=True)
        return

    spin_locks.add(chat_id)
    bot.answer_callback_query(call.id)  # –∑–∞–∫—Ä—ã—Ç—å –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä
    threading.Thread(target=_run_quick_spin, args=(call, user_id, bet)).start()

def _run_quick_spin(call, user_id, bet):
    chat_id = call.message.chat.id
    msg_id = call.message.message_id
    try:
        # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –ø–æ–ª–Ω—É—é 3x3 –º–∞—Ç—Ä–∏—Ü—É, –Ω–æ –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ç–æ–ª—å–∫–æ —Å—Ä–µ–¥–Ω—é—é —Å—Ç—Ä–æ–∫—É
        final = spin_once()
        middle = final[1]  # –Ω–∞–ø—Ä–∏–º–µ—Ä ["‚≠ê", "üçâ", "7Ô∏è‚É£"]

        # –û—Ü–µ–Ω–∫–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ –ø–æ —Å—Ä–µ–¥–Ω–µ–π —Å—Ç—Ä–æ–∫–µ
        result, mult = eval_middle_row(final)

        # –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –±–∞–ª–∞–Ω—Å–∞: —Å–ø–∏—Å–∞—Ç—å —Å—Ç–∞–≤–∫—É –∏ –¥–æ–±–∞–≤–∏—Ç—å –≤—ã–∏–≥—Ä—ã—à –ø—Ä–∏ –ø–æ–±–µ–¥–µ
        bal = get_balance(user_id)
        bal -= bet
        if result != "lose":
            win = bet * mult
            bal += win
        set_balance(user_id, bal)

        # –§–æ—Ä–º–∞—Ç –≤—ã–≤–æ–¥–∞: —Ä–æ–≤–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ —Å —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª–µ–º " | "
        middle_text = " | ".join(middle)  # "‚≠ê | üçâ | 7Ô∏è‚É£"

        if result == "lose":
            suffix = (
                f"\n\n‚ùå <b>–£–≤—ã‚Ä¶ –∫–æ–º–±–∏–Ω–∞—Ü–∏—è –Ω–µ —Å–æ–≤–ø–∞–ª–∞.</b>\n"
                f"üí∞ <b>–ë–∞–ª–∞–Ω—Å:</b> {bal} –º–æ–Ω–µ—Ç"
            )
        else:
            suffix = (
                f"\n\nüéâ <b>–í—ã –≤—ã–∏–≥—Ä–∞–ª–∏!</b>\n"
                f"‚ú® <b>–í—ã–∏–≥—Ä—ã—à:</b> √ó{mult}\n"
                f"üí∞ <b>–ë–∞–ª–∞–Ω—Å:</b> {bal} –º–æ–Ω–µ—Ç"
            )

        text = f"<b>–í—ã–ø–∞–ª–æ:</b>\n{middle_text}{suffix}"
        bot.edit_message_text(text, chat_id, msg_id, reply_markup=result_kb(), parse_mode="HTML")
    except Exception:
        try:
            bot.edit_message_text("–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ —Ä–∞—Å—á—ë—Ç–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.", chat_id, msg_id)
        except:
            pass
    finally:
        spin_locks.discard(chat_id)

